var AgentMonitor = class extends llmonitor_default {
  name;
  agentRunId;
  constructor(options = {}) {
    super(options);
    this.name = options.name;
  }
  trackEvent(type, data = {}) {
    return super.trackEvent(type, {
      ...data,
      agentRunId: this.agentRunId
    });
  }
  /*
   * Wrap an agent Promise to track it's input, results and any errors.
   * @param {Promise} func - Agent function
   */
  wrapExecutor(func) {
    return async (...args) => {
      this.agentRunId = crypto.randomUUID();
      const argNames = getArgumentNames(func);
      const input = argNames.reduce((obj, argName, index) => {
        obj[argName] = args[index];
        return obj;
      }, {});
      this.agentStart({
        name: this.name,
        input,
        agentRunId: this.agentRunId
      });
      try {
        const result = await func(...args);
        this.agentEnd({
          output: result,
          agentRunId: this.agentRunId
        });
        return result;
      } catch (error) {
        this.agentError({
          error,
          agentRunId: this.agentRunId
        });
        throw error;
      } finally {
        this.agentRunId = void 0;
      }
    };
  }
};

